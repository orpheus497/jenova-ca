##Script function and purpose: Memory Systems package for The JENOVA Cognitive Architecture
##This package implements the multi-layered memory architecture:
##- Episodic Memory: Stores conversation history and experiences (episodic.py)
##- Semantic Memory: Stores facts and knowledge (semantic.py)
##- Procedural Memory: Stores procedures and how-to knowledge (procedural.py)
##All memory systems use ChromaDB for vector storage and retrieval
